package model;

/**
 *
 * @author HP
 */
public class Stock {

    private String ticker;
    private String name;
    private String sector;
    private double price;
    private int status;

    public Stock() {
    }

    public Stock(String ticker, String name, String sector, double price, int status) {
        this.ticker = ticker;
        this.name = name;
        this.sector = sector;
        this.price = price;
        this.status = status;
    }

    public String getTicker() {
        return ticker;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSector() {
        return sector;
    }

    public void setSector(String sector) {
        this.sector = sector;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
